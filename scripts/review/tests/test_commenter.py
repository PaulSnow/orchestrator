class TestPostReviewComment:
    """Tests for post_review_comment."""

    def test_posts_to_github(self):
        """Post review to GitHub issue."""
        review = {"cycle": 1}

        with patch("review.commenter.get_platform_client") as mock_get_client:
            mock_client = MagicMock()
            mock_client.post_comment.return_value = True
            mock_get_client.return_value = mock_client

            result = post_review_comment("github", "owner/repo", 42, review)

        assert result is True
        mock_client.post_comment.assert_called_once()
        call_args = mock_client.post_comment.call_args
        assert call_args[0][0] == "owner/repo"
        assert call_args[0][1] == 42
        assert "Orchestrator Review" in call_args[0][2]

    def test_posts_to_gitlab(self):
        """Post review to GitLab issue."""
        review = {"cycle": 1}

        with patch("review.commenter.get_platform_client") as mock_get_client:
            mock_client = MagicMock()
            mock_client.post_comment.return_value = True
            mock_get_client.return_value = mock_client

            result = post_review_comment("gitlab", "group/repo", 42, review)

        assert result is True
        mock_get_client.assert_called_once_with("gitlab", None)

    def test_passes_token(self):
        """Pass token to client."""
        review = {"cycle": 1}

        with patch("review.commenter.get_platform_client") as mock_get_client:
            mock_client = MagicMock()
            mock_client.post_comment.return_value = True
            mock_get_client.return_value = mock_client

            post_review_comment("github", "owner/repo", 42, review, token="my-token")

        mock_get_client.assert_called_once_with("github", "my-token")

    def test_returns_false_on_failure(self):
        """Return False when posting fails."""
        review = {"cycle": 1}

        with patch("review.commenter.get_platform_client") as mock_get_client:
            mock_client = MagicMock()
            mock_client.post_comment.return_value = False
            mock_get_client.return_value = mock_client

            result = post_review_comment("github", "owner/repo", 42, review)

        assert result is False
